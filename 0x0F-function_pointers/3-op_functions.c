/**
 * op_add - function that sum
 * @a: firts number
 * @b: second number
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub- function that difference of a and b
 * @a: firts number
 * @b: second number
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function that mul
 * @a: firts number
 * @b: second number
 * Return: the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function that division
 * @a: firts number
 * @b: second number
 * Return: the result of division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function that % of a by b
 * @a: firts number
 * @b: second number
 * Return: the remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
